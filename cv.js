function fillData() {
    fillNameAndTitle()
    fillContact()
    fillObjective()
    fillProjects()
}

function fillNameAndTitle() {
    const { full_name, title } = data
    document.getElementById('full_name').innerText = full_name
    document.getElementById('title').innerText = title
}

function fillContact() {
    let html = ''
    const { info_contacts } = data
    info_contacts.forEach((info) => {
        html += '<div class="info-contact-item">'
        html += `<div class="icon-info"><i class="${info.icon}"></i></div>`
        html += `<a target="_blank" href="${info.action}">${info.text}</a>`
        html += '</div>'
    })
    document.getElementById('info-contact').innerHTML = html
}

function fillObjective() {
    const { objectives } = data
    let html = '<div class="title-block">'
    html += objectives.title
    html += '</div>'
    html += '<div class="content-block">'

    objectives.items.forEach((item) => {
        html += `<span class="mb-6">${item}</span>`
    })
    html += '</div>'
    document.getElementById('about').innerHTML = html
}

function fillProjects() {
    let html = '<div class="title-block">PROJECTS</div>'
    const { projects } = data
    projects.forEach((project) => {
        html += '<div class="content-block">'
        html += '<div class="flex items-center">'
        html += `<span class="font-bold text-16">${project.name}</span>`
        html += `<span class="ml-6">(${project.time})</span>`
        html += '</div>'
        html += '<table class="my-12">'

        const { details } = project
        details.forEach((detail) => {
            html += '<tr>'
            html += '<td class="w-250">'
            html += detail.label
            html += '</td>'
            html += '<td>'

            if (detail.text instanceof Array) {
                html += '<div class="flex-col">'
                detail.text.forEach((txt) => {
                    html += `<span>- ${txt}</span>`
                })
                html += '</div>'
            } else {
                html += detail.text
            }
        })
        html += '</table>'
        html += '</div>'
    })
    document.getElementById('projects-block').innerHTML = html
}
